package videos_curtos;

public class Unarios {

	public static void main(String[] args) {
		
		int joey = 0;
		
//		Antes da função (pré incremento)
		System.out.println(++joey);
		System.out.println(joey);
		
//		Depois da função (pós incremento)
		System.out.println(joey++);
		
// 		Duas horas depois...
		System.out.println(joey);

	}

}
